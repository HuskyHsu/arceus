export const GetTypeIcon = (type: string) => {
  const iconSize = "w-5 h-5";
  if (type === "蟲") return <Bug className={iconSize} />;
  if (type === "惡") return <Dark className={iconSize} />;
  if (type === "龍") return <Dragon className={iconSize} />;
  if (type === "電") return <Electric className={iconSize} />;
  if (type === "妖精") return <Fairy className={iconSize} />;
  if (type === "格鬥") return <Fighting className={iconSize} />;
  if (type === "火") return <Fire className={iconSize} />;
  if (type === "飛行") return <Flying className={iconSize} />;
  if (type === "幽靈") return <Ghost className={iconSize} />;
  if (type === "草") return <Grass className={iconSize} />;
  if (type === "地面") return <Ground className={iconSize} />;
  if (type === "冰") return <Ice className={iconSize} />;
  if (type === "一般") return <Normal className={iconSize} />;
  if (type === "毒") return <Poison className={iconSize} />;
  if (type === "超能力") return <Psychic className={iconSize} />;
  if (type === "岩石") return <Rock className={iconSize} />;
  if (type === "鋼") return <Steel className={iconSize} />;
  if (type === "水") return <Water className={iconSize} />;
};
